<?php
namespace App\Http\Traits;

trait CheckTrait
{
    /**
     * 验证码校验
     * @param $code
     * @param $vcode
     * @return bool
     */
    public function checkVcode($tel, $code, $vcode)
    {
        // 白名单不校验
        $isDebug = config('app.debug');
        $whiteList = [
            '13500000001',
            '13500000002',
            '13500000003',
            '13500000004',
            '13500000005',
            '13500000006',
            '13500000007',
            '13500000008',
            '13500000009',
            '13500000011',
            '13500000012',
            '13500000013',
            '13500000014',
            '13500000015',
            '13500000016',
            '13500000017',
            '13500000018',
            '13500000019',
            '13500000020',
            '13500000021',
            '13500000022',
            '13500000023',
            '13500000024',
            '13500000025',
            '13500000026',
            '13500000027',
            '13500000028',
            '13500000029',
            '13500000030',
            '13500000031',
            '13500000032',
            '13500000033',
            '13500000034',
            '13500000035'
        ];
        if (in_array($tel, $whiteList) && $isDebug) {
            return true;
        }

        if (trim($code) == trim($vcode)) {
            return true;
        } else {
            return false;
        }
    }
}
